package eproject.easyTickect.object;

import java.util.Date;

/**
 *
 * @author Trang
 */
public class DateTimes {

    private int id;
    private Date date;
    private Date time;

    public DateTimes() {
    }

    public DateTimes(int id, Date date, Date time) {
        this.id = id;
        this.date = date;
        this.time = time;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }

}
